			That is how Light Paint Live: Instant was born. It had two controls, and the developer deemed it finished.
		</p>
		<p>
			The story goes on, though, and an Android app was created. It was soon discovered by Joerg Miedza, a very prominent member of the light-painting community. The app was very limited, but Miedza saw it as the next step in the evolution of light-painting. He wanted to know if it could be improved, so he emailed the developer.
		</p>
